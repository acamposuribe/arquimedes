"""Vault scaffolding and inspection helpers (Phase 10).

A *vault* is a private git repo containing one knowledge base's data tree
(`extracted/`, `manifests/`, `derived/`, `wiki/`, plus its own `config/`).
Code lives in the public `arquimedes` package, installed via pipx; vaults are
distinct repos pointed at via the global `--config` flag, the
`$ARQUIMEDES_CONFIG` env var, or cwd-walk.

This module owns:
- `init_vault()` — scaffold an empty vault on disk (no remote, no library).
- `vault_info()` — report the resolved vault root, library root, local cache
  root, freshness, config source, and vault git remote.
"""

